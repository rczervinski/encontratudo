export function buscarCidade(termo: string): { nome: string; lat: number; lng: number; estado: string } | null {
  const termoNormalizado = termo.toLowerCase().trim()
  
  // Verificar aliases
  const cidadeAlias = aliasesCidades[termoNormalizado]
  const termoBusca = cidadeAlias || termo
  
  // Buscar em todas as cidades
  for (const [estado, cidades] of Object.entries(cidadesBrasileiras)) {
    for (const cidade of cidades) {
      if (cidade.nome.toLowerCase().includes(termoBusca.toLowerCase())) {
        return { ...cidade, estado }
      }
    }
  }
  
  return null
}

export function todasCidades(): Array<{ nome: string; estado: string; lat: number; lng: number }> {
  const todas: Array<{ nome: string; estado: string; lat: number; lng: number }> = []
  
  for (const [estado, cidades] of Object.entries(cidadesBrasileiras)) {
    for (const cidade of cidades) {
      todas.push({ ...cidade, estado })
    }
  }
  
  return todas.sort((a, b) => a.nome.localeCompare(b.nome))
}
